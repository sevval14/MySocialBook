public class friend {
    private int id;
    private String name;
    private String username;
    private String age;
    private String school;

    public friend(int id, String name, String username, String age, String school) {
        this.id = id;
        this.name = name;
        this.username = username;
        this.age = age;
        this.school = school;
    }
}
